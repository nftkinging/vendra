"              <button onClick={() => setMode('circle')} className='v4btn v4btn-ink' style={{ width: '100%', justifyContent: 'center' }}>Log in with Circle wallet \u2192</button>",
"            </div>",
"            <div style={{ textAlign: 'center', marginTop: 8 }}><Link href='/marketplace' className='jn-browse'>Browse without connecting \u2192</Link></div>",
