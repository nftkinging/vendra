"    } catch (e:any) { setError(e.message || 'Send failed'); }",
"    finally { setSending(false); }",
"  };",
"",
"  return (",
"    <main className='v4home jn-main'>",
"      <Nav theme='v4' />",
"      <div className='amb'><div className='amb-glow' /><div className='amb-grid' /></div>",
"      <div className='jn-in'>",
"        <div className='jn-head'>",
"          <div className='vbadge'><span className='vbadge-dot' /><span className='vbadge-txt'>Arc Testnet \u00b7 Powered by USDC</span></div>",
"          <h1 className='jn-h1'>Join <span className='v4amber'>Vendra</span></h1>",
"          <p className='jn-sub'>Connect a Web3 wallet or log in with Circle \u2014 no crypto experience needed.</p>",
"        </div>",
"        {mode === 'options' && (",
"          <div style={{ display: 'flex', flexDirection: 'column', gap: 12 }}>",
"            <div className='jn-card'>",
"              <div className='jn-card-l'>\uD83E\uDD8A Web3 wallet</div>",
"              <div className='jn-card-p'>MetaMask, Coinbase, WalletConnect or any EVM wallet.</div>",
"              <div className='jn-connect'><ConnectButton label='Connect Wallet' accountStatus='address' chainStatus='none' showBalance={false} /></div>",
"            </div>",
"            <div className='vor'><div className='vor-line' /><span className='vor-txt'>or</span><div className='vor-line' /></div>",
"            <div className='jn-card'>",
