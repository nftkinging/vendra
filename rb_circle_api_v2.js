const fs = require('fs');

fs.mkdirSync('app/api/circle/create-wallet', { recursive: true });
fs.mkdirSync('app/api/circle/balance', { recursive: true });
fs.mkdirSync('app/api/circle/send', { recursive: true });
fs.mkdirSync('app/api/circle/login', { recursive: true });

// ── Login / Get wallet by email ──────────────────────────────────────────────
const login = [];
login.push("import { NextRequest, NextResponse } from 'next/server';");
login.push("import { initiateDeveloperControlledWalletsClient } from '@circle-fin/developer-controlled-wallets';");
login.push("");
login.push("export async function POST(req: NextRequest) {");
login.push("  try {");
login.push("    const { email } = await req.json();");
login.push("    if (!email) return NextResponse.json({ error: 'Email required' }, { status: 400 });");
login.push("    const client = initiateDeveloperControlledWalletsClient({");
login.push("      apiKey: process.env.CIRCLE_API_KEY!,");
login.push("      entitySecret: process.env.CIRCLE_ENTITY_SECRET!,");
login.push("    });");
login.push("    // Search for existing wallet by refId (email)");
login.push("    const wallets = (await client.listWallets({ refId: email })).data?.wallets;");
login.push("    if (wallets && wallets.length > 0) {");
login.push("      const wallet = wallets[0];");
login.push("      return NextResponse.json({ found: true, walletId: wallet.id, address: wallet.address });");
login.push("    }");
login.push("    return NextResponse.json({ found: false });");
login.push("  } catch (e: any) {");
login.push("    return NextResponse.json({ error: e.message }, { status: 500 });");
login.push("  }");
login.push("}");
fs.writeFileSync('app/api/circle/login/route.ts', login.join('\n'), 'utf8');
console.log('login route done');

// ── Create Wallet ────────────────────────────────────────────────────────────
const create = [];
create.push("import { NextRequest, NextResponse } from 'next/server';");
create.push("import { initiateDeveloperControlledWalletsClient } from '@circle-fin/developer-controlled-wallets';");
create.push("");
create.push("export async function POST(req: NextRequest) {");
create.push("  try {");
create.push("    const { email } = await req.json();");
create.push("    if (!email) return NextResponse.json({ error: 'Email required' }, { status: 400 });");
create.push("    const client = initiateDeveloperControlledWalletsClient({");
create.push("      apiKey: process.env.CIRCLE_API_KEY!,");
create.push("      entitySecret: process.env.CIRCLE_ENTITY_SECRET!,");
create.push("    });");
create.push("    const wallet = (await client.createWallets({");
create.push("      walletSetId: process.env.CIRCLE_WALLET_SET_ID!,");
