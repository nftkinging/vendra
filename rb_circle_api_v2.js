create.push("    return NextResponse.json({ walletId: wallet.id, address: wallet.address });");
create.push("  } catch (e: any) {");
create.push("    return NextResponse.json({ error: e.message }, { status: 500 });");
create.push("  }");
create.push("}");
fs.writeFileSync('app/api/circle/create-wallet/route.ts', create.join('\n'), 'utf8');
console.log('create-wallet route done');

