lines.push("                    <button onClick={handleCopy} className={copied?'btn-primary':'btn-slate'} style={{width:'100%',fontSize:10,padding:'10px'}}>{copied?'✓ Address Copied!':'Copy Address'}</button>");
lines.push("                  </div>");
lines.push("                  <div style={{display:'flex',alignItems:'center',gap:10,marginBottom:14}}><div style={{flex:1,height:1,background:'var(--b1)'}}/><span style={{fontSize:9,fontWeight:300,fontStyle:'italic',color:'var(--w18)',letterSpacing:'0.12em',textTransform:'uppercase'}}>or</span><div style={{flex:1,height:1,background:'var(--b1)'}}/></div>");
lines.push("                  <a href='https://faucet.circle.com/' target='_blank' rel='noopener noreferrer'><button className='btn-ghost' style={{width:'100%',fontSize:10,padding:'10px'}}>💧 Get Free Test USDC ↗</button></a>");
lines.push("                </div>)}");
lines.push("              {tab==='bridge' && (");
lines.push("                <div>");
lines.push("                  <div style={{fontSize:11,fontWeight:300,fontStyle:'italic',color:'var(--w35)',lineHeight:1.7,marginBottom:12}}>Bridge USDC to your Circle wallet on Arc Testnet.</div>");
lines.push("                  <div style={{fontSize:9,fontWeight:300,fontStyle:'italic',letterSpacing:'0.14em',textTransform:'uppercase',color:'var(--w18)',marginBottom:8}}>From Chain</div>");
lines.push("                  <div style={{display:'grid',gridTemplateColumns:'repeat(5,1fr)',gap:4,marginBottom:12}}>");
lines.push("                    {CHAINS.map(c=><button key={c.id} onClick={()=>setFromChain(c.id)} style={{padding:'8px 2px',textAlign:'center',background:fromChain===c.id?'rgba(155,181,200,0.12)':'var(--bg3)',border:fromChain===c.id?'1px solid var(--sl)':'1px solid var(--b1)',cursor:'pointer',transition:'all 0.2s',display:'flex',flexDirection:'column',alignItems:'center',gap:3}}>");
lines.push("                      <img src={c.logo} alt={c.label} style={{width:22,height:22,borderRadius:'50%',objectFit:'cover',border:'1px solid var(--b1)'}} onError={e=>{(e.target as HTMLImageElement).style.opacity='0.3';}}/>");
lines.push("                      <span style={{fontFamily:\"'DM Sans',sans-serif\",fontSize:7,fontWeight:300,letterSpacing:'0.06em',textTransform:'uppercase',color:fromChain===c.id?'var(--sl)':'var(--w18)'}}>{c.label}</span>");
lines.push("                    </button>)}");
lines.push("                  </div>");
lines.push("                  <div style={{display:'flex',alignItems:'center',gap:8,marginBottom:12}}>");
lines.push("                    <div style={{flex:1,display:'flex',alignItems:'center',gap:6,border:'1px solid var(--b1)',padding:'8px 10px',background:'var(--bg3)'}}>");
lines.push("                      <img src={selectedChain.logo} alt={selectedChain.label} style={{width:18,height:18,borderRadius:'50%',objectFit:'cover'}} onError={e=>{(e.target as HTMLImageElement).style.display='none';}}/>");
lines.push("                      <span style={{fontSize:10,fontWeight:300,fontStyle:'italic',color:'var(--w60)'}}>{selectedChain.label}</span>");
lines.push("                    </div>");
lines.push("                    <span style={{color:'var(--sl)',fontSize:'1.1rem'}}>→</span>");
lines.push("                    <div style={{flex:1,display:'flex',alignItems:'center',gap:6,border:'1px solid rgba(155,181,200,0.3)',padding:'8px 10px',background:'rgba(155,181,200,0.06)'}}>");
lines.push("                      <span style={{fontSize:10,fontWeight:300,fontStyle:'italic',color:'var(--sl2)'}}>Arc Testnet</span>");
lines.push("                    </div>");
lines.push("                  </div>");
lines.push("                  <div style={{fontSize:9,fontWeight:300,fontStyle:'italic',letterSpacing:'0.14em',textTransform:'uppercase',color:'var(--w18)',marginBottom:8}}>Amount (USDC)</div>");
lines.push("                  <div style={{display:'grid',gridTemplateColumns:'repeat(4,1fr)',gap:4,marginBottom:8}}>");
lines.push("                    {AMOUNTS.map(a=><button key={a} onClick={()=>setBridgeAmount(a)} style={{fontFamily:\"'Cormorant',serif\",fontSize:14,fontWeight:300,padding:'8px 4px',textAlign:'center',background:bridgeAmount===a?'rgba(155,181,200,0.12)':'var(--bg3)',border:bridgeAmount===a?'1px solid var(--sl)':'1px solid var(--b1)',color:bridgeAmount===a?'var(--sl2)':'var(--w35)',cursor:'pointer',transition:'all 0.2s'}}>${a}</button>)}");
lines.push("                  </div>");
lines.push("                  <input className='v-input' type='number' placeholder='Custom amount...' value={bridgeAmount} onChange={e=>setBridgeAmount(e.target.value)} style={{marginBottom:12}}/>");
lines.push("                  {bridgeError&&<div style={{fontSize:10,fontStyle:'italic',color:'var(--err)',marginBottom:10,border:'1px solid rgba(232,112,112,0.2)',padding:'7px 10px'}}>{bridgeError}</div>}");
lines.push("                  {bridgeResult&&<div style={{fontSize:10,fontStyle:'italic',color:'var(--gr)',marginBottom:10,border:'1px solid var(--gr3)',padding:'7px 10px'}}>{bridgeResult}</div>}");
lines.push("                  <button onClick={handleBridge} disabled={bridgeLoading} className='btn-slate' style={{width:'100%',padding:'11px',fontSize:10}}>{bridgeLoading?'Bridging...':`Bridge $${bridgeAmount} USDC → Arc`}</button>");
lines.push("                </div>)}");
lines.push("            </div>");
lines.push("          </div>");
lines.push("        </>");
lines.push("      )}");
lines.push("    </div>");
lines.push("  );");
lines.push("}");
fs.writeFileSync('app/components/CircleBalanceWidget.tsx', lines.join('\n'), 'utf8');
