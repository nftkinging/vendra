lines.push("import { useState, useEffect } from 'react';");
lines.push("");
lines.push("type Tab = 'balance'|'deposit'|'bridge';");
lines.push("const CHAINS = [");
lines.push("  { id:'Ethereum_Sepolia', label:'Ethereum', logo:'https://cryptologos.cc/logos/ethereum-eth-logo.png' },");
lines.push("  { id:'Base_Sepolia', label:'Base', logo:'https://cryptologos.cc/logos/base-logo.png' },");
lines.push("  { id:'Arbitrum_Sepolia', label:'Arbitrum', logo:'https://cryptologos.cc/logos/arbitrum-arb-logo.png' },");
