lines.push("  { id:'Solana_Devnet', label:'Solana', logo:'https://cryptologos.cc/logos/solana-sol-logo.png' },");
lines.push("];");
lines.push("const AMOUNTS = ['1','5','10','50'];");
lines.push("");
lines.push("interface Props {");
lines.push("  wallet: { address: string; walletId: string; email: string };");
lines.push("}");
lines.push("");
lines.push("export default function CircleBalanceWidget({ wallet }: Props) {");
