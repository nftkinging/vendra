lines.push("  const [tab, setTab] = useState<Tab>('balance');");
lines.push("  const [balance, setBalance] = useState('0.00');");
lines.push("  const [copied, setCopied] = useState(false);");
lines.push("  const [fromChain, setFromChain] = useState('Ethereum_Sepolia');");
lines.push("  const [bridgeAmount, setBridgeAmount] = useState('10');");
lines.push("  const [bridgeLoading, setBridgeLoading] = useState(false);");
lines.push("  const [bridgeResult, setBridgeResult] = useState('');");
lines.push("  const [bridgeError, setBridgeError] = useState('');");
lines.push("");
lines.push("  useEffect(() => { fetchBalance(); }, [wallet.walletId]);");
lines.push("");
lines.push("  const fetchBalance = async () => {");
lines.push("    try {");
lines.push("      const res = await fetch('/api/circle/balance', {");
lines.push("        method: 'POST',");
lines.push("        headers: { 'Content-Type': 'application/json' },");
lines.push("        body: JSON.stringify({ walletId: wallet.walletId }),");
lines.push("      });");
lines.push("      const data = await res.json();");
lines.push("      setBalance(parseFloat(data.balance || '0').toFixed(2));");
lines.push("    } catch {}");
lines.push("  };");
lines.push("");
lines.push("  const handleCopy = () => {");
lines.push("    navigator.clipboard.writeText(wallet.address);");
lines.push("    setCopied(true); setTimeout(() => setCopied(false), 2000);");
lines.push("  };");
lines.push("");
lines.push("  const handleBridge = async () => {");
lines.push("    if (!bridgeAmount) { setBridgeError('Enter an amount'); return; }");
lines.push("    setBridgeLoading(true); setBridgeError(''); setBridgeResult('');");
lines.push("    try {");
lines.push("      const res = await fetch('/api/circle/bridge', {");
lines.push("        method: 'POST',");
lines.push("        headers: { 'Content-Type': 'application/json' },");
lines.push("        body: JSON.stringify({ fromChain, toChain: 'Arc_Testnet', amount: bridgeAmount, walletAddress: wallet.address }),");
lines.push("      });");
lines.push("      const data = await res.json();");
lines.push("      if (data.error) throw new Error(data.error);");
lines.push("      setBridgeResult('Bridge initiated! USDC is on its way to Arc Testnet.');");
lines.push("    } catch(e:any) {");
lines.push("      setBridgeError(e?.message || 'Bridge failed.');");
lines.push("    } finally { setBridgeLoading(false); }");
lines.push("  };");
lines.push("");
lines.push("  const selectedChain = CHAINS.find(c => c.id === fromChain) || CHAINS[0];");
lines.push("  const tabs: {id:Tab;label:string}[] = [{id:'balance',label:'Balance'},{id:'deposit',label:'Deposit'},{id:'bridge',label:'Bridge'}];");
lines.push("");
lines.push("  return (");
lines.push("    <div style={{ position:'relative' }}>");
lines.push("      <button onClick={() => { setOpen(!open); if (!open) fetchBalance(); }} style={{ fontFamily:\"'DM Sans',sans-serif\", fontSize:11, fontWeight:400, background:'var(--bg2)', border:'1px solid var(--b2)', color:'var(--sl2)', padding:'8px 14px', cursor:'pointer', display:'flex', alignItems:'center', gap:8, transition:'all 0.3s' }} onMouseEnter={e=>(e.currentTarget.style.borderColor='var(--sl)')} onMouseLeave={e=>(e.currentTarget.style.borderColor='var(--b2)')}>"); 
lines.push("        <span>⭕</span>");
lines.push("        <span style={{fontFamily:\"'Cormorant',serif\",fontSize:16,fontWeight:300}}>${balance}</span>");
lines.push("        <span style={{fontSize:9,fontWeight:300,fontStyle:'italic',letterSpacing:'0.10em',textTransform:'uppercase',color:'var(--w18)'}}>USDC</span>");
lines.push("        <span style={{fontSize:8,color:'var(--w18)',display:'inline-block',transform:open?'rotate(180deg)':'rotate(0)'}}>▼</span>");
lines.push("      </button>");
lines.push("      {open && (");
lines.push("        <>");
lines.push("          <div onClick={()=>setOpen(false)} style={{position:'fixed',inset:0,zIndex:98}}/>");
lines.push("          <div style={{position:'absolute',top:'calc(100% + 8px)',right:0,width:320,background:'var(--bg2)',border:'1px solid var(--b2)',zIndex:99,boxShadow:'0 20px 60px rgba(0,0,0,0.6)',overflow:'hidden'}}>");
lines.push("            <div style={{position:'absolute',top:0,left:'10%',right:'10%',height:1,background:'linear-gradient(90deg,transparent,rgba(155,181,200,0.5),transparent)'}}/>");
lines.push("            <div style={{padding:'12px 16px',borderBottom:'1px solid var(--b1)',display:'flex',justifyContent:'space-between',alignItems:'center'}}>");
lines.push("              <div style={{fontSize:9,fontWeight:300,fontStyle:'italic',letterSpacing:'0.16em',textTransform:'uppercase',color:'var(--sl)'}}>Circle Wallet · USDC</div>");
lines.push("              <button onClick={()=>setOpen(false)} style={{background:'transparent',border:'none',color:'var(--w18)',cursor:'pointer'}}>✕</button>");
lines.push("            </div>");
lines.push("            <div style={{display:'flex',borderBottom:'1px solid var(--b1)'}}>");
lines.push("              {tabs.map(t=><button key={t.id} onClick={()=>{setTab(t.id);setBridgeError('');setBridgeResult('');}} style={{flex:1,fontFamily:\"'DM Sans',sans-serif\",fontSize:9,fontWeight:300,fontStyle:'italic',letterSpacing:'0.12em',textTransform:'uppercase',padding:'10px 4px',background:'transparent',border:'none',borderBottom:tab===t.id?'1px solid var(--sl)':'1px solid transparent',color:tab===t.id?'var(--sl)':'var(--w18)',cursor:'pointer',transition:'all 0.2s'}}>{t.label}</button>)}");
lines.push("            </div>");
lines.push("            <div style={{padding:16}}>");
lines.push("              {tab==='balance' && (");
lines.push("                <div style={{textAlign:'center'}}>");
lines.push("                  <div style={{fontSize:9,fontWeight:300,fontStyle:'italic',letterSpacing:'0.16em',textTransform:'uppercase',color:'var(--w18)',marginBottom:4}}>Circle Wallet · USDC Balance</div>");
lines.push("                  <div style={{fontFamily:\"'Cormorant',serif\",fontSize:40,fontWeight:300,color:'var(--sl2)',lineHeight:1,marginBottom:2}}>${balance}</div>");
lines.push("                  <div style={{fontSize:9,fontWeight:300,fontStyle:'italic',color:'var(--w18)',marginBottom:14}}>USDC · Arc Testnet</div>");
lines.push("                  <div style={{display:'flex',gap:8,marginBottom:8}}>");
lines.push("                    <button onClick={()=>setTab('deposit')} className='btn-slate' style={{flex:1,fontSize:9,padding:'9px'}}>Deposit ↓</button>");
lines.push("                    <button onClick={()=>setTab('bridge')} className='btn-ghost' style={{flex:1,fontSize:9,padding:'9px'}}>Bridge 🌉</button>");
lines.push("                  </div>");
lines.push("                  <div style={{display:'flex',gap:8}}>");
lines.push("                    <a href='https://faucet.circle.com/' target='_blank' rel='noopener noreferrer' style={{flex:1}}><button className='btn-ghost' style={{width:'100%',fontSize:9,padding:'8px'}}>💧 Faucet</button></a>");
lines.push("                    <a href={`https://testnet.arcscan.app/address/${wallet.address}`} target='_blank' rel='noopener noreferrer' style={{flex:1}}><button className='btn-ghost' style={{width:'100%',fontSize:9,padding:'8px'}}>Explorer ↗</button></a>");
lines.push("                  </div>");
lines.push("                </div>)}");
lines.push("              {tab==='deposit' && (");
lines.push("                <div>");
lines.push("                  <div style={{fontSize:11,fontWeight:300,fontStyle:'italic',color:'var(--w35)',lineHeight:1.7,marginBottom:14}}>Copy your Circle wallet address to receive USDC on Arc Testnet.</div>");
lines.push("                  <div style={{border:'1px solid var(--b1)',background:'var(--bg3)',padding:'14px',marginBottom:14}}>");
lines.push("                    <div style={{fontSize:9,fontWeight:300,fontStyle:'italic',letterSpacing:'0.14em',textTransform:'uppercase',color:'var(--sl)',marginBottom:6}}>Circle Wallet · Arc Testnet</div>");
