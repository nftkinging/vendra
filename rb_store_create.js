lines.push("              </div>");
lines.push("            </div>");
lines.push("            {/* Wallet */}");
lines.push("            <div style={{border:'1px solid var(--b1)',marginBottom:28}}>");
lines.push("              <div className='v-block-head'>Payment Wallet · ERC-8004 Identity</div>");
lines.push("              <div style={{padding:'16px 20px'}}>");
lines.push("                <div style={{fontSize:9,fontWeight:300,fontStyle:'italic',letterSpacing:'0.18em',textTransform:'uppercase',color:'var(--w18)',marginBottom:6}}>Receives USDC payments from buyers</div>");
lines.push("                <div style={{fontFamily:\"'Cormorant',serif\",fontSize:16,fontWeight:300,color:'var(--a2)'}}>{address?address.slice(0,6)+'...'+address.slice(-4)+' · Arc Testnet':'Connect your wallet'}</div>");
lines.push("              </div>");
lines.push("            </div>");
lines.push("            {error&&<div style={{border:'1px solid rgba(232,112,112,0.3)',background:'rgba(232,112,112,0.08)',padding:'12px 16px',fontSize:12,fontWeight:300,fontStyle:'italic',color:'var(--err)',marginBottom:20}}>{error}</div>}");
lines.push("            <button onClick={handleCreate} disabled={loading} className='btn-primary' style={{width:'100%',padding:'18px',fontSize:12}}>{'Deploy Store on Arc · $'}{FEE}{' USDC →'}</button>");
