const fs = require('fs');
fs.mkdirSync('app/store/create', { recursive: true });
const lines = [];
lines.push("'use client';");
lines.push("import Nav from '../../Nav';");
lines.push("import { useState, useRef } from 'react';");
lines.push("import { useAccount } from 'wagmi';");
lines.push("import { useRouter } from 'next/navigation';");
lines.push("import { saveStore, getStoreByWallet, uploadImage } from '../../lib/supabase';");
lines.push("import { useSendTransaction } from 'wagmi';");
lines.push("import { parseUnits } from 'viem';");
lines.push("import Link from 'next/link';");
lines.push("");
