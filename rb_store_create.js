lines.push("  const [step, setStep] = useState<'form'|'paying'|'success'>('form');");
lines.push("  const [error, setError] = useState('');");
lines.push("  const [deployedStore, setDeployedStore] = useState<any>(null);");
lines.push("  const [loading, setLoading] = useState(false);");
