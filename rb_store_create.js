lines.push("  const storeUrl = 'https://vendra-app-omega.vercel.app/store/'+slug;");
lines.push("  const tweet = 'I just deployed my store \"'+form.name+'\" on Arc Testnet!\\n\\n'+storeUrl+'\\n\\n#ArcTestnet #Web3 #Vendra';");
lines.push("");
lines.push("  const handleBanner = (e: React.ChangeEvent<HTMLInputElement>) => {");
lines.push("    const f = e.target.files?.[0]; if (!f) return;");
lines.push("    setBannerFile(f); setBannerPreview(URL.createObjectURL(f));");
lines.push("  };");
lines.push("");
lines.push("  const handleCreate = async () => {");
lines.push("    if (!address || !isConnected) { setError('Please connect your wallet'); return; }");
lines.push("    if (!form.name) { setError('Store name is required'); return; }");
lines.push("    setLoading(true); setError('');");
lines.push("    try {");
lines.push("      const existing = await getStoreByWallet(address);");
lines.push("      if (existing) { setError('You already have a store. Delete it first.'); setLoading(false); return; }");
lines.push("      setStep('paying');");
lines.push("      const hash = await sendTransactionAsync({ to: FEE_WALLET, value: parseUnits(FEE.toString(), 18) });");
lines.push("      let bannerUrl = '';");
lines.push("      if (bannerFile && address) bannerUrl = await uploadImage('banners/'+address, bannerFile);");
lines.push("      const store = await saveStore({ owner_wallet:address, name:form.name, tagline:form.tagline, description:form.description, category:cat, slug, x_handle:form.xHandle, deploy_fee_tx:hash, banner_url:bannerUrl });");
lines.push("      setDeployedStore(store); setStep('success');");
lines.push("    } catch(e:any) {");
lines.push("      setStep('form');");
lines.push("      setError(e?.message?.includes('rejected')?'Transaction rejected.':'Something went wrong. Please try again.');");
lines.push("    } finally { setLoading(false); }");
lines.push("  };");
lines.push("");
lines.push("  return (");
