lines.push("            <div style={{fontFamily:\"'Cormorant',serif\",fontSize:'clamp(32px,5vw,52px)',fontWeight:300,color:'var(--w)',marginBottom:16}}>Deploying Store</div>");
lines.push("            <div style={{fontSize:13,fontWeight:300,fontStyle:'italic',color:'var(--w35)',marginBottom:32}}>Approve the {'$'}{FEE}{' USDC deployment fee in your wallet'}</div>");
lines.push("            <div className='v-spinner' style={{margin:'0 auto'}}/>");
lines.push("          </div>)}");
lines.push("");
lines.push("        {step==='success'&&(");
lines.push("          <div style={{textAlign:'center',padding:'3rem 0'}}>");
lines.push("            <div style={{fontFamily:\"'Cormorant',serif\",fontSize:'5rem',fontWeight:300,color:'var(--a2)',lineHeight:1,marginBottom:16}}>✓</div>");
lines.push("            <div style={{fontFamily:\"'Cormorant',serif\",fontSize:'clamp(32px,5vw,52px)',fontWeight:300,color:'var(--w)',marginBottom:12}}>Store Deployed</div>");
lines.push("            <div style={{fontSize:13,fontWeight:300,fontStyle:'italic',color:'var(--w35)',marginBottom:36,lineHeight:1.7}}><strong style={{color:'var(--w)'}}>{form.name}</strong>{' is now live on Arc Testnet'}</div>");
lines.push("            <div style={{display:'flex',flexDirection:'column',gap:12,maxWidth:280,margin:'0 auto'}}>");
lines.push("              <a href={'https://twitter.com/intent/tweet?text='+encodeURIComponent(tweet)} target='_blank' rel='noopener noreferrer'><button className='btn-ghost' style={{width:'100%',padding:'14px'}}>Share on 𝕏</button></a>");
lines.push("              <Link href={'/store/'+(deployedStore?.slug||slug)}><button className='btn-primary' style={{width:'100%',padding:'14px'}}>View My Store →</button></Link>");
lines.push("              <Link href='/store/edit'><button className='btn-amber-ghost' style={{width:'100%',padding:'14px'}}>Add Products →</button></Link>");
lines.push("            </div>");
lines.push("          </div>)}");
lines.push("");
lines.push("        {step==='form'&&(");
lines.push("          <>");
lines.push("            <div className='v-eyebrow' style={{marginBottom:24}}><div className='v-eyebrow-rule'/><span className='v-eyebrow-label'>Arc Testnet</span></div>");
lines.push("            <h1 style={{fontFamily:\"'Cormorant',serif\",fontSize:'clamp(40px,6vw,72px)',fontWeight:300,letterSpacing:'-0.01em',lineHeight:0.94,color:'var(--w)',marginBottom:12}}>Launch Your<br/><em style={{fontStyle:'italic',background:'linear-gradient(120deg,var(--a),var(--a2))',WebkitBackgroundClip:'text',WebkitTextFillColor:'transparent',backgroundClip:'text'}}>Store</em></h1>");
lines.push("            <p style={{fontSize:13,fontWeight:300,fontStyle:'italic',color:'var(--w35)',marginBottom:8,lineHeight:1.7}}>Set up in 2 minutes. Sell globally, get paid instantly.</p>");
lines.push("            <div style={{display:'inline-flex',alignItems:'center',gap:8,border:'1px solid rgba(212,176,90,0.2)',padding:'8px 16px',marginBottom:40}}>");
lines.push("              <div className='v-arc-badge-dot'/>");
lines.push("              <span style={{fontSize:10,fontWeight:300,fontStyle:'italic',letterSpacing:'0.14em',textTransform:'uppercase',color:'var(--a)'}}>{'Deployment fee: $'}{FEE}{' USDC · Arc Testnet'}</span>");
lines.push("            </div>");
lines.push("            {/* Banner */}");
lines.push("            <div style={{border:'1px solid var(--b1)',marginBottom:28,overflow:'hidden'}}>");
lines.push("              <div className='v-block-head'>Store Banner</div>");
lines.push("              <div style={{padding:20}}>");
lines.push("                <input ref={bannerRef} type='file' accept='image/*' onChange={handleBanner} style={{display:'none'}}/>"); 
lines.push("                {bannerPreview");
lines.push("                  ? <div style={{position:'relative'}}><img src={bannerPreview} alt='banner' style={{width:'100%',height:160,objectFit:'cover',display:'block',filter:'brightness(0.7)'}}/><button onClick={()=>bannerRef.current?.click()} style={{position:'absolute',bottom:12,right:12,fontFamily:\"'DM Sans',sans-serif\",fontSize:9,fontWeight:300,letterSpacing:'0.14em',textTransform:'uppercase',color:'var(--w35)',border:'1px solid var(--b2)',padding:'6px 12px',background:'rgba(12,14,26,0.85)',cursor:'pointer'}}>Change</button></div>");
lines.push("                  : <div onClick={()=>bannerRef.current?.click()} className='v-upload-zone' style={{height:140}}><div style={{fontSize:'2rem'}}>{'🖼️'}</div><div className='v-upload-label'>Click to upload banner</div><div style={{fontSize:9,fontWeight:300,fontStyle:'italic',color:'var(--w18)',letterSpacing:'0.10em'}}>Recommended: 1200×400px</div></div>}");
lines.push("              </div>");
lines.push("            </div>");
lines.push("            {/* Identity */}");
lines.push("            <div style={{border:'1px solid var(--b1)',marginBottom:28}}>");
lines.push("              <div className='v-block-head'>Store Identity</div>");
lines.push("              <div style={{padding:20}}>");
lines.push("                <div className='v-field'><label className='v-label'>Store name *</label><input className='v-input' type='text' placeholder='e.g. Nour Atelier' value={form.name} onChange={e=>setForm({...form,name:e.target.value})}/></div>");
lines.push("                <div className='v-field'><label className='v-label'>Tagline</label><input className='v-input' type='text' placeholder='One line about what you sell' value={form.tagline} onChange={e=>setForm({...form,tagline:e.target.value})}/></div>");
lines.push("                <div className='v-field'><label className='v-label'>Description</label><textarea className='v-textarea' placeholder='Tell buyers what makes your store special...' value={form.description} onChange={e=>setForm({...form,description:e.target.value})}/></div>");
lines.push("                <div className='v-field' style={{marginBottom:0}}><label className='v-label'>X (Twitter) handle</label><input className='v-input' type='text' placeholder='@yourhandle' value={form.xHandle} onChange={e=>setForm({...form,xHandle:e.target.value})}/></div>");
lines.push("              </div>");
lines.push("            </div>");
lines.push("            {/* Category */}");
lines.push("            <div style={{border:'1px solid var(--b1)',marginBottom:28}}>");
lines.push("              <div className='v-block-head'>Category</div>");
